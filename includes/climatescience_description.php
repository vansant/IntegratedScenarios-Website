<p>The NOAA Climate Prediction Center (CPC) has been providing the <a href="http://www.cpc.ncep.noaa.gov/products/NMME">North American Multi-Model Ensemble (NMME)</a> forecasts to support monthly outlooks and monitoring activities over the Contiguous United States since 2012. The current NMME system consists of 8 model forecasts from US and Canada modeling center, each of which 
provides forecasts for 8 month lead periods from the current month for a set of ensemble runs. 
In order to assess the skill of each model, hindcast forecasts are available from 1982-2010.
